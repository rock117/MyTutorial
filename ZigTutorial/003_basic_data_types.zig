// ============================================================================
// 003_basic_data_types.zig
// ============================================================================
// 教程主题：基本数据类型
// 难度等级：⭐ (新手)
// 涵盖知识点：
//   - 整数类型（有符号/无符号）
//   - 浮点类型
//   - 布尔类型
//   - 字符类型
//   - 类型范围和溢出
// ============================================================================

const std = @import("std");

pub fn main() !void {
    const stdout = std.io.getStdOut().writer();

    // ========================================================================
    // 1. 整数类型 (Integer Types)
    // ========================================================================
    // 整数类型分为有符号 (signed) 和无符号 (unsigned)
    // 命名规则：i/N 或 u/N，其中 N 是位数

    // 有符号整数（可以表示负数）
    const i8_val: i8 = 127;         // 8 位有符号：-128 到 127
    const i16_val: i16 = 1000;      // 16 位有符号：-32768 到 32767
    const i32_val: i32 = 100000;    // 32 位有符号：约 -21亿 到 21亿
    const i64_val: i64 = 1000000;   // 64 位有符号：非常大的范围

    // 无符号整数（只表示非负数）
    const u8_val: u8 = 255;         // 8 位无符号：0 到 255
    const u16_val: u16 = 1000;      // 16 位无符号：0 到 65535
    const u32_val: u32 = 4000000;   // 32 位无符号：0 到 约42亿
    const u64_val: u64 = 10000000;  // 64 位无符号：极大的范围

    try stdout.print("整数类型示例：\n", .{});
    try stdout.print("  i8:  {d:5}  (范围: -128 到 127)\n", .{i8_val});
    try stdout.print("  i16: {d:5}  (范围: -32768 到 32767)\n", .{i16_val});
    try stdout.print("  i32: {d:5}  (范围: 约 ±21亿)\n", .{i32_val});
    try stdout.print("  i64: {d:5}  (范围: 非常大)\n", .{i64_val});
    try stdout.print("  u8:  {d:5}  (范围: 0 到 255)\n", .{u8_val});
    try stdout.print("  u16: {d:5}  (范围: 0 到 65535)\n", .{u16_val});
    try stdout.print("  u32: {d:5}  (范围: 0 到 约42亿)\n", .{u32_val});
    try stdout.print("  u64: {d:5}  (范围: 极大)\n", .{u64_val});
    try stdout.print("\n", .{});

    // ========================================================================
    // 2. 浮点类型 (Floating Point Types)
    // ========================================================================
    // 浮点类型用于表示小数

    const f16_val: f16 = 3.14;      // 16 位浮点（半精度）
    const f32_val: f32 = 3.14159;   // 32 位浮点（单精度）
    const f64_val: f64 = 2.71828;   // 64 位浮点（双精度）
    const f128_val: f128 = 1.414;   // 128 位浮点（四倍精度）

    try stdout.print("浮点类型示例：\n", .{});
    try stdout.print("  f16:  {d:.5}\n", .{f16_val});
    try stdout.print("  f32:  {d:.5}\n", .{f32_val});
    try stdout.print("  f64:  {d:.5}\n", .{f64_val});
    try stdout.print("  f128: {d:.5}\n", .{f128_val});
    try stdout.print("\n", .{});

    // ========================================================================
    // 3. 布尔类型 (Boolean Type)
    // ========================================================================
    // 布尔类型只有两个值：true 和 false

    const is_true: bool = true;
    const is_false: bool = false;

    // 布尔运算
    const and_result: bool = true and false;   // false
    const or_result: bool = true or false;     // true
    const not_result: bool = !true;             // false

    try stdout.print("布尔类型示例：\n", .{});
    try stdout.print("  is_true = {}\n", .{is_true});
    try stdout.print("  is_false = {}\n", .{is_false});
    try stdout.print("  true and false = {}\n", .{and_result});
    try stdout.print("  true or false  = {}\n", .{or_result});
    try stdout.print("  not true       = {}\n", .{not_result});
    try stdout.print("\n", .{});

    // ========================================================================
    // 4. 字符类型 (Character Types)
    // ========================================================================
    // Zig 中字符实际上是整数类型
    // ASCII 字符使用 u8，Unicode 字符使用 u21

    const ascii_char: u8 = 'A';        // ASCII 字符
    const digit_char: u8 = '0';        // 数字字符
    const newline: u8 = '\n';          // 转义字符

    // Unicode 字符（需要更大的类型）
    const unicode_char: u21 = '中';    // Unicode 字符

    try stdout.print("字符类型示例：\n", .{});
    try stdout.print("  'A' (ASCII)     = {c} (数值: {d})\n", .{ascii_char, ascii_char});
    try stdout.print("  '0' (digit)     = {c} (数值: {d})\n", .{digit_char, digit_char});
    try stdout.print("  '\\n' (newline)  = ASCII 值 {d}\n", .{newline});
    try stdout.print("  '中' (Unicode)  = U+{X:0>4}\n", .{unicode_char});
    try stdout.print("\n", .{});

    // ========================================================================
    // 5. 特殊整数值
    // ========================================================================

    const max_i8: i8 = 127;           // i8 最大值
    const min_i8: i8 = -128;          // i8 最小值
    const max_u8: u8 = 255;           // u8 最大值
    const min_u8: u8 = 0;             // u8 最小值

    try stdout.print("特殊整数值：\n", .{});
    try stdout.print("  i8 最大值: {d}\n", .{max_i8});
    try stdout.print("  i8 最小值: {d}\n", .{min_i8});
    try stdout.print("  u8 最大值: {d}\n", .{max_u8});
    try stdout.print("  u8 最小值: {d}\n", .{min_u8});
    try stdout.print("\n", .{});

    // ========================================================================
    // 6. 类型字面量 (Type Literals)
    // ========================================================================
    // 使用不同的进制表示整数

    const decimal: i32 = 42;          // 十进制
    const hex: i32 = 0x2A;            // 十六进制 (0x 前缀)
    const octal: i32 = 0o52;          // 八进制 (0o 前缀)
    const binary: i32 = 0b101010;     // 二进制 (0b 前缀)

    try stdout.print("不同进制的整数（都等于 42）：\n", .{});
    try stdout.print("  十进制:   {d}\n", .{decimal});
    try stdout.print("  十六进制: 0x{X:0>2} = {d}\n", .{hex, hex});
    try stdout.print("  八进制:   0o{o:0>2} = {d}\n", .{octal, octal});
    try stdout.print("  二进制:   0b{b:0>8} = {d}\n", .{binary, binary});
    try stdout.print("\n", .{});

    // ========================================================================
    // 7. 数字分隔符
    // ========================================================================
    // 使用 _ 分隔数字提高可读性

    const million: i32 = 1_000_000;           // 100 万
    const binary_sep: i32 = 0b1010_1100;      // 分隔的二进制
    const credit_card: u64 = 1234_5678_9012_3456;  // 信用卡号

    try stdout.print("数字分隔符示例：\n", .{});
    try stdout.print("  100 万: {d}\n", .{million});
    try stdout.print("  二进制: 0b{b}\n", .{binary_sep});
    try stdout.print("  信用卡: {d}\n", .{credit_card});
}

// ============================================================================
// 运行方式：
//   zig run 003_basic_data_types.zig
// ============================================================================

// ============================================================================
// 关键概念解释：
//
// 1. 类型大小：
//    - iN/uN：N 位整数，例如 i8 是 8 位（1 字节）
//    - fN：N 位浮点数
//    - 位数的不同决定了数值范围和精度
//
// 2. 有符号 vs 无符号：
//    - 有符号 (i)：可以表示正数和负数
//    - 无符号 (u)：只能表示非负数
//    - 同样位数，无符号可以表示更大的正数
//
// 3. 浮点精度：
//    - f32：约 7 位十进制精度
//    - f64：约 16 位十进制精度
//    - 通常使用 f32 或 f64
//
// 4. 字符编码：
//    - ASCII：7 位编码，表示英文字符（0-127）
//    - Unicode：21 位编码，表示所有语言的字符
//    - Zig 使用 u8 表示 ASCII，u21 表示 Unicode
// ============================================================================

// ============================================================================
// 常见问题：
//
// Q: 如何选择合适的整数类型？
// A: 根据数据范围选择：
//    - 如果值在 -128 到 127 之间，用 i8
//    - 如果值在 0 到 255 之间，用 u8
//    - 一般情况，i32 和 u32 是好的默认选择
//
// Q: 浮点数有精度问题吗？
// A: 是的。浮点数不能精确表示某些小数（如 0.1）。
//    对于需要精确计算的场景（如货币），建议使用整数。
//
// Q: 什么时候用字符类型？
// A: 处理单个字符时。对于字符串，使用 []const u8（字符数组/切片）。
// ============================================================================

