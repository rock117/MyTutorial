// 导入 Zig 标准库
// @import 是编译时内置函数，用于导入模块
// "std" 是标准库的名称
const std = @import("std");

// pub 表示"公开"（public），这个函数可以被其他模块访问
// fn 表示"函数"（function）
// main 是程序入口点，程序执行从这里开始
// !void 是返回类型：
//   - void 表示没有返回值
//   - ! 表示可能返回错误（error union type）
pub fn main() !void {
    // 获取标准输出流
    // std.io.getStdOut() 返回标准输出文件
    // .writer() 获取输出流的写入器
    const stdout = std.io.getStdOut().writer();

    // try 是错误处理关键字
    // 如果函数返回错误，try 会立即返回错误
    // 如果成功，继续执行后续代码
    // print 是格式化打印函数
    // 第一个参数是格式字符串
    // 第二个参数是格式化参数列表（使用 .{} 语法）
    try stdout.print("Hello, World! 你好，世界！\n", .{});

    // 更多打印示例
    try stdout.print("欢迎来到 Zig 的世界！\n", .{});
    try stdout.print("Zig 是一门系统级编程语言。\n", .{});
}

// ============================================================================
// 运行方式：
//   zig run 001_hello_world.zig
