//   Hello, World! 你好，世界！
//   欢迎来到 Zig 的世界！
//   Zig 是一门系统级编程语言。
// ============================================================================

// ============================================================================
// 关键概念解释：
//
// 1. const (常量)：
//    const 声明的值不可改变
//    用于声明不会改变的变量
//
// 2. @import (导入)：
//    编译时函数，导入其他 Zig 文件或模块
//    返回一个包含导入模块所有公开内容的结构体
//
// 3. !void (错误联合类型)：
//    !T 是 Zig 的特殊语法
//    表示返回值可能是错误，也可能是类型 T 的值
//    !void 表示：要么返回错误，要么成功返回（无实际返回值）
//
// 4. try (错误处理)：
//    如果表达式返回错误，立即从当前函数返回该错误
//    如果成功，提取出实际值继续执行
//    相当于其他语言的 ? 或 await 的错误处理版本
//
// 5. .{} (匿名结构体字面量)：
//    用于传递格式化参数
//    即使没有参数也需要传递空的 .{}
// ============================================================================

// ============================================================================
// 常见问题：
//
// Q: 为什么要用 .{} 而不是 {} ?
// A: 在 Zig 中，{} 是块语法，.{} 是空的匿名结构体字面量。
//    print 函数要求第二个参数必须是结构体类型。
//
// Q: 为什么 main 函数返回 !void 而不是 void?
// A: 因为我们的函数内部使用了 try，可能返回错误。
//    如果函数内部不会返回错误，应该用 void 而不是 !void。
//
// Q: 什么是标准输出 (stdout)?
// A: 标准输出是进程默认的输出流，通常是终端/控制台。
// ============================================================================

