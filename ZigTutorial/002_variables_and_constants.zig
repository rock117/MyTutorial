// ============================================================================
// 002_variables_and_constants.zig
// ============================================================================
// 教程主题：变量与常量
// 难度等级：⭐ (新手)
// 涵盖知识点：
//   - const 声明常量
//   - var 声明变量
//   - 类型注解
//   - 类型推断
// ============================================================================

const std = @import("std");

pub fn main() !void {
    const stdout = std.io.getStdOut().writer();

    // ========================================================================
    // 1. const - 常量声明
    // ========================================================================
    // const 声明的值不可改变（编译时不可变）
    // 必须在声明时初始化

    // 显式类型注解
    const pi: f64 = 3.14159;

    // 类型推断（编译器自动推断类型）
    const max_connections = 100;  // 推断为 i32 (整数默认类型)

    // 常量用于表示不会改变的值
    const app_name = "我的 Zig 程序";

    try stdout.print("常量示例：\n", .{});
    try stdout.print("  π = {d:.5}\n", .{pi});
    try stdout.print("  最大连接数 = {d}\n", .{max_connections});
    try stdout.print("  应用名称 = {s}\n", .{app_name});
    try stdout.print("\n", .{});

    // ========================================================================
    // 2. var - 变量声明
    // ========================================================================
    // var 声明的值可以改变
    // 用于表示会改变的数据

    var counter: i32 = 0;  // 计数器
    var score: f32 = 0.0;  // 分数

    try stdout.print("变量示例：\n", .{});
    try stdout.print("  初始计数器 = {d}\n", .{counter});
    try stdout.print("  初始分数 = {d:.1}\n", .{score});

    // 修改变量的值
    counter = 10;
    score = 95.5;

    try stdout.print("  修改后计数器 = {d}\n", .{counter});
    try stdout.print("  修改后分数 = {d:.1}\n", .{score});
    try stdout.print("\n", .{});

    // ========================================================================
    // 3. 类型推断示例
    // ========================================================================

    // 整数默认推断为 comptime_int（编译时整数）
    const inferred_int = 42;

    // 浮点数默认推断为 comptime_float（编译时浮点）
    const inferred_float = 3.14;

    // 布尔值
    const is_active = true;

    // 字符类型
    const first_char: u8 = 'A';

    try stdout.print("类型推断示例：\n", .{});
    try stdout.print("  整数 = {d}\n", .{inferred_int});
    try stdout.print("  浮点 = {d:.2}\n", .{inferred_float});
    try stdout.print("  布尔 = {}\n", .{is_active});
    try stdout.print("  字符 = {c}\n", .{first_char});
    try stdout.print("\n", .{});

    // ========================================================================
    // 4. 变量运算示例
    // ========================================================================

    var a: i32 = 10;
    var b: i32 = 20;

    try stdout.print("变量运算示例：\n", .{});
    try stdout.print("  a = {d}, b = {d}\n", .{a, b});
    try stdout.print("  a + b = {d}\n", .{a + b});
    try stdout.print("  a * b = {d}\n", .{a * b});

    // 修改变量值
    a = a + 5;  // a 变成 15
    b *= 2;     // b 变成 40

    try stdout.print("  修改后 a = {d}, b = {d}\n", .{a, b});
    try stdout.print("\n", .{});

    // ========================================================================
    // 5. 作用域演示
    // ========================================================================
    // 注意：Zig 不允许变量遮蔽，每个变量名在作用域内必须唯一

    const x: i32 = 100;
    try stdout.print("作用域示例：\n", .{});
    try stdout.print("  外部 x = {d}\n", .{x});

    {
        // 在这个块中，我们使用不同的变量名
        const y: i32 = 200;
        try stdout.print("  内部 y = {d}\n", .{y});
    }

    try stdout.print("  外部 x (未改变) = {d}\n", .{x});
    try stdout.print("\n", .{});

    // ========================================================================
    // 6. undefined - 未定义值
    // ========================================================================
    // undefined 表示值未定义，可以用于任何类型
    // 用于声明变量但不初始化的情况（不推荐，除非必要）

    var uninitialized: i32 = undefined;
    uninitialized = 999;

    try stdout.print("undefined 示例：\n", .{});
    try stdout.print("  初始化后的值 = {d}\n", .{uninitialized});
}

// ============================================================================
// 运行方式：
//   zig run 002_variables_and_constants.zig
// ============================================================================

// ============================================================================
// 关键概念解释：
//
// 1. const vs var：
//    - const：常量，不可变，必须在声明时初始化
//    - var：变量，可变，可以稍后修改
//
// 2. 类型注解：
//    显式指定类型：const x: i32 = 10;
//    类型推断：const x = 10;  // 编译器推断为 i32
//
// 3. 标识符命名：
//    - 变量名使用 snake_case：my_variable
//    - 常量名也使用 snake_case：max_count
//    - 类型名使用 PascalCase（后续会讲到）
//
// 4. 变量作用域：
//    - 变量只在声明它的块内有效
//    - 内部块可以遮蔽外部块的变量
// ============================================================================

// ============================================================================
// 常见问题：
//
// Q: 什么时候用 const，什么时候用 var?
// A: 优先使用 const。只有当值需要改变时才用 var。
//    const 使代码更安全、更易理解。
//
// Q: const 声明的值是编译时常量吗？
// A: 不一定。运行时计算的值也可以用 const 声明。
//    const 只表示运行时不可变，不一定是编译时常量。
//
// Q: 为什么有时需要显式类型注解？
// A: 类型推断可能推断出你不想用的类型，比如
//    const x = 10;  // 可能推断为 comptime_int
//    const x: i32 = 10;  // 明确指定为 i32
// ============================================================================

