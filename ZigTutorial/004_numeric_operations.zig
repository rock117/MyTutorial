// ============================================================================
// 004_numeric_operations.zig
// ============================================================================
// 教程主题：数值运算
// 难度等级：⭐ (新手)
// 涵盖知识点：
//   - 算术运算符
//   - 比较运算符
//   - 位运算符
//   - 数学函数
// ============================================================================

const std = @import("std");

pub fn main() !void {
    const stdout = std.io.getStdOut().writer();

    // ========================================================================
    // 1. 算术运算符 (Arithmetic Operators)
    // ========================================================================

    const a: i32 = 20;
    const b: i32 = 8;

    try stdout.print("算术运算（a = {d}, b = {d}）：\n", .{a, b});
    try stdout.print("  加法 a + b  = {d}\n", .{a + b});      // 28
    try stdout.print("  减法 a - b  = {d}\n", .{a - b});      // 12
    try stdout.print("  乘法 a * b  = {d}\n", .{a * b});      // 160
    try stdout.print("  除法 a / b  = {d}\n", .{a / b});      // 2 (整数除法)
    try stdout.print("  取模 a % b  = {d}\n", .{a % b});      // 4
    try stdout.print("\n", .{});

    // ========================================================================
    // 2. 浮点运算
    // ========================================================================

    const x: f64 = 10.0;
    const y: f64 = 3.0;

    try stdout.print("浮点运算（x = {d:.1}, y = {d:.1}）：\n", .{x, y});
    try stdout.print("  加法 x + y  = {d:.3}\n", .{x + y});
    try stdout.print("  减法 x - y  = {d:.3}\n", .{x - y});
    try stdout.print("  乘法 x * y  = {d:.3}\n", .{x * y});
    try stdout.print("  除法 x / y  = {d:.3}\n", .{x / y});
    try stdout.print("\n", .{});

    // ========================================================================
    // 3. 复合赋值运算符
    // ========================================================================

    var num: i32 = 10;

    try stdout.print("复合赋值运算（初始值 = {d}）：\n", .{num});

    num += 5;   // num = num + 5  => 15
    try stdout.print("  num += 5   => {d}\n", .{num});

    num -= 3;   // num = num - 3  => 12
    try stdout.print("  num -= 3   => {d}\n", .{num});

    num *= 2;   // num = num * 2  => 24
    try stdout.print("  num *= 2   => {d}\n", .{num});

    num = @divTrunc(num, 4);   // num = num / 4  => 6
    try stdout.print("  num /= 4   => {d}\n", .{num});

    num = @rem(num, 4);   // num = num % 4  => 2
    try stdout.print("  num %= 4   => {d}\n", .{num});
    try stdout.print("\n", .{});

    // ========================================================================
    // 4. 比较运算符 (Comparison Operators)
    // ========================================================================
    // 比较运算符返回布尔值

    const m: i32 = 10;
    const n: i32 = 20;

    try stdout.print("比较运算（m = {d}, n = {d}）：\n", .{m, n});
    try stdout.print("  m == n (相等)     = {}\n", .{m == n});    // false
    try stdout.print("  m != n (不等)     = {}\n", .{m != n});    // true
    try stdout.print("  m < n  (小于)     = {}\n", .{m < n});     // true
    try stdout.print("  m > n  (大于)     = {}\n", .{m > n});     // false
    try stdout.print("  m <= n (小于等于) = {}\n", .{m <= n});    // true
    try stdout.print("  m >= n (大于等于) = {}\n", .{m >= n});    // false
    try stdout.print("\n", .{});

    // ========================================================================
    // 5. 位运算符 (Bitwise Operators)
    // ========================================================================
    // 位运算直接操作整数的二进制位

    const p: u8 = 0b1010_1000;  // 168
    const q: u8 = 0b1100_0100;  // 196

    try stdout.print("位运算（p = 0b{b:0>8} = {d}, q = 0b{b:0>8} = {d}）：\n", .{p, p, q, q});

    // AND: 两个位都为 1 时结果为 1
    try stdout.print("  p & q  (AND)    = 0b{b:0>8} = {d}\n", .{p & q, p & q});

    // OR: 两个位有一个为 1 时结果为 1
    try stdout.print("  p | q  (OR)     = 0b{b:0>8} = {d}\n", .{p | q, p | q});

    // XOR: 两个位不同时结果为 1
    try stdout.print("  p ^ q  (XOR)    = 0b{b:0>8} = {d}\n", .{p ^ q, p ^ q});

    // NOT: 按位取反
    try stdout.print("  ~p     (NOT)    = 0b{b:0>8} = {d}\n", .{~p, ~p});

    // 左移：所有位向左移动
    try stdout.print("  p << 2 (左移)   = 0b{b:0>8} = {d}\n", .{p << 2, p << 2});

    // 右移：所有位向右移动
    try stdout.print("  p >> 2 (右移)   = 0b{b:0>8} = {d}\n", .{p >> 2, p >> 2});
    try stdout.print("\n", .{});

    // ========================================================================
    // 6. 递增和递减
    // ========================================================================
    // Zig 没有 ++ 或 -- 运算符，需要使用 += 1 或 -= 1

    var counter: i32 = 0;
    try stdout.print("递增递减示例：\n", .{});
    try stdout.print("  初始值: {d}\n", .{counter});

    counter += 1;  // 递增
    try stdout.print("  递增后: {d}\n", .{counter});

    counter -= 1;  // 递减
    try stdout.print("  递减后: {d}\n", .{counter});
    try stdout.print("\n", .{});

    // ========================================================================
    // 7. 数学函数
    // ========================================================================
    // 使用 std.math 模块中的数学函数

    const abs_val: i32 = @abs(-42);  // 绝对值
    const min_val: i32 = @min(10, 20);          // 最小值
    const max_val: i32 = @max(10, 20);          // 最大值
    const clamped: i32 = @max(0, @min(15, 10));     // 限制在范围内

    try stdout.print("数学函数：\n", .{});
    try stdout.print("  abs(-42)       = {d}\n", .{abs_val});
    try stdout.print("  min(10, 20)    = {d}\n", .{min_val});
    try stdout.print("  max(10, 20)    = {d}\n", .{max_val});
    try stdout.print("  clamp(15,0,10) = {d}\n", .{clamped});
    try stdout.print("\n", .{});

    // ========================================================================
    // 8. 浮点数学函数
    // ========================================================================

    const float_val: f64 = 2.0;
    const sqrt_val = std.math.sqrt(float_val);      // 平方根
    const pow_val = std.math.pow(f64, 3.0, 2.0);   // 幂运算 (3^2)
    const floor_val = std.math.floor(3.7);          // 向下取整
    const ceil_val = std.math.ceil(3.2);            // 向上取整
    const round_val = std.math.round(3.5);          // 四舍五入

    try stdout.print("浮点数学函数：\n", .{});
    try stdout.print("  sqrt(2.0)     = {d:.5}\n", .{sqrt_val});
    try stdout.print("  pow(3, 2)     = {d:.5}\n", .{pow_val});
    try stdout.print("  floor(3.7)    = {d:.1}\n", .{floor_val});
    try stdout.print("  ceil(3.2)     = {d:.1}\n", .{ceil_val});
    try stdout.print("  round(3.5)    = {d:.1}\n", .{round_val});
}

// ============================================================================
// 运行方式：
//   zig run 004_numeric_operations.zig
// ============================================================================

// ============================================================================
// 关键概念解释：
//
// 1. 整数除法：
//    整数除法会舍去小数部分，不是四舍五入
//    例如：7 / 3 = 2，不是 2.33
//
// 2. 运算符优先级：
//    括号 > 乘除模 > 加减 > 比较运算符 > 逻辑运算符
//    不确定时使用括号明确优先级
//
// 3. 位运算应用：
//    - &：用于掩码操作
//    - |：用于设置位
//    - ^：用于翻转位
//    - <<：乘以 2 的幂
//    - >>：除以 2 的幂
//
// 4. 浮点精度：
//    浮点运算可能有精度误差，比较时要小心
//    使用 std.math.approxEq 进行近似比较
// ============================================================================

// ============================================================================
// 常见问题：
//
// Q: 为什么没有 ++ 和 -- 运算符？
// A: Zig 设计简洁，+= 和 -= 已经够用，避免不必要的复杂性。
//
// Q: 整数溢出会怎样？
// A: Debug 模式会 panic，Release 模式会回绕（wrap around）。
//    使用 overflowing 运算符显式处理溢出。
//
// Q: 如何比较浮点数？
// A: 由于精度问题，不要直接用 == 比较。
//    使用 std.math.approxEq 进行近似比较。
// ============================================================================

