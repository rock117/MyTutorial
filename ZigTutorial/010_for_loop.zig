//   - for 与数组
//   - for 与切片
//   - for 与范围
//   - break 和 continue
// ============================================================================

const std = @import("std");

pub fn main() !void {
    const stdout = std.io.getStdOut().writer();

    // ========================================================================
    // 1. 基本 for 循环
    // ========================================================================

    const fruits = [_][]const u8{ "苹果", "香蕉", "橙子" };

    try stdout.print("基本 for 循环：\n", .{});
    for (fruits) |fruit| {
        try stdout.print("  - {s}\n", .{fruit});
    }
    try stdout.print("\n", .{});

    // ========================================================================
    // 2. for 带索引
    // ========================================================================

    const colors = [_][]const u8{ "红", "绿", "蓝", "黄" };

    try stdout.print("for 带索引：\n", .{});
    for (colors, 0..) |color, index| {
        try stdout.print("  [{d}] {s}\n", .{index, color});
    }
    try stdout.print("\n", .{});

    // ========================================================================
    // 3. for 与范围
    // ========================================================================
    // 使用 .. 创建范围

    try stdout.print("for 与范围：\n", .{});
    for (0..5) |n| {
        try stdout.print("  数字: {d}\n", .{n});
    }
    try stdout.print("\n", .{});

    // 带索引的范围
    try stdout.print("带索引的范围：\n", .{});
    for (0..5, 0..) |n, index| {
        try stdout.print("  索引 {d}: 值 {d}\n", .{index, n});
    }
    try stdout.print("\n", .{});

    // ========================================================================
    // 4. for 与切片
    // ========================================================================

    const numbers = [_]i32{ 10, 20, 30, 40, 50, 60, 70 };
    const slice = numbers[1..6];  // [20, 30, 40, 50, 60]

    try stdout.print("for 与切片：\n", .{});
    try stdout.print("  原数组: {any}\n", .{numbers});
    try stdout.print("  切片: {any}\n", .{slice});
    for (slice, 0..) |value, index| {
        try stdout.print("    [{d}] = {d}\n", .{index, value});
    }
    try stdout.print("\n", .{});

    // ========================================================================
    // 5. for 与多维数组
    // ========================================================================

    const matrix: [3][3]i32 = [_][3]i32{
        [_]i32{ 1, 2, 3 },
        [_]i32{ 4, 5, 6 },
        [_]i32{ 7, 8, 9 },
    };

    try stdout.print("for 与多维数组：\n", .{});
    for (matrix, 0..) |row, row_index| {
        try stdout.print("  行 {d}: ", .{row_index});
        for (row) |cell| {
            try stdout.print("{d} ", .{cell});
        }
        try stdout.print("\n", .{});
    }
    try stdout.print("\n", .{});

    // ========================================================================
    // 6. for 中的 break 和 continue
    // ========================================================================

    try stdout.print("for 中的 break：\n", .{});
    for (0..10) |i| {
        if (i == 5) {
            try stdout.print("  在 {d} 处停止\n", .{i});
            break;
        }
        try stdout.print("  {d}\n", .{i});
    }
    try stdout.print("\n", .{});

    try stdout.print("for 中的 continue：\n", .{});
    for (0..5) |i| {
        if (i == 2) {
            try stdout.print("  跳过 {d}\n", .{i});
            continue;
        }
        try stdout.print("  {d}\n", .{i});
    }
    try stdout.print("\n", .{});

    // ========================================================================
    // 7. 带标签的 break/continue
    // ========================================================================

    try stdout.print("嵌套 for 循环（带标签）：\n", .{});

    outer: for (0..3) |i| {
        try stdout.print("  外层 {d}: ", .{i});

        for (0..3) |j| {
            if (i == 1 and j == 1) {
                try stdout.print("跳过\n", .{});
                continue :outer;
            }
            try stdout.print("{d} ", .{j});
        }
        try stdout.print("\n", .{});
    }
    try stdout.print("\n", .{});

    // ========================================================================
    // 8. for 与枚举
    // ========================================================================

    const Day = enum { Sunday, Monday, Tuesday, Wednesday, Thursday, Friday, Saturday };
    const day_names = [_][]const u8{
        "星期日", "星期一", "星期二", "星期三",
        "星期四", "星期五", "星期六",
    };

    try stdout.print("for 与枚举：\n", .{});
    inline for (std.meta.fields(Day), 0..) |field, index| {
        try stdout.print("  {s}: {s}\n", .{field.name, day_names[index]});
    }
}

// ============================================================================
// 运行方式：
//   zig run 010_for_loop.zig
// ============================================================================

// ============================================================================
// 关键概念解释：
//
// 1. 基本 for 循环：
//    for (iterable) |item| {
//        // 循环体
//    }
//
// 2. for 带索引：
//    for (iterable, 0..) |item, index| {
//        // 循环体
//    }
//
// 3. for 与范围：
//    for (start..end) |value| {
//        // 循环体
//    }
//    end 不包含在内（[start, end)）
//
// 4. for 与多维数组：
//    外层 for 遍历行
//    内层 for 遍历列
//
// 5. 标签：
//    label: for (...) {
//        break :label;    // 跳出标签指定的循环
//        continue :label; // 继续标签指定的循环
//    }
// ============================================================================

