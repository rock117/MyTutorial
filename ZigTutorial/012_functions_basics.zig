const std = @import("std");

// ============================================================================
// 1. 无返回值函数
// ============================================================================
// 返回类型为 void 表示没有返回值
fn greet(name: []const u8) void {
    std.debug.print("Hello, {s}!\n", .{name});
}

// ============================================================================
// 2. 有返回值的函数
// ============================================================================
// 返回类型为 i32，返回整数
fn add(a: i32, b: i32) i32 {
    return a + b;
}

// ============================================================================
// 3. 多参数函数
// ============================================================================
fn multiply(a: i32, b: i32, c: i32) i32 {
    return a * b * c;
}

// ============================================================================
// 4. 带类型推断的返回值
// ============================================================================
// 使用编译时类型推断
fn double(x: i32) i32 {
    return x * 2;
}

// ============================================================================
// 5. 函数作为表达式
// ============================================================================
fn square(x: i32) i32 {
    return x * x;
}

pub fn main() !void {
    const stdout = std.io.getStdOut().writer();

    // ========================================================================
    // 测试无返回值函数
    // ========================================================================
    try stdout.print("=== Function Basics Demo ===\n\n", .{});

    try stdout.print("Calling greet function:\n", .{});
    greet("Alice");
    greet("Bob");

    try stdout.print("\n", .{});

    // ========================================================================
    // 测试有返回值函数
    // ========================================================================
    try stdout.print("Calling add function:\n", .{});
    const sum = add(10, 20);
    try stdout.print("  add(10, 20) = {d}\n", .{sum});

    const sum2 = add(5, 7);
    try stdout.print("  add(5, 7) = {d}\n", .{sum2});

    try stdout.print("\n", .{});

    // ========================================================================
    // 测试多参数函数
    // ========================================================================
    try stdout.print("Calling multiply function:\n", .{});
    const product = multiply(2, 3, 4);
    try stdout.print("  multiply(2, 3, 4) = {d}\n", .{product});

    try stdout.print("\n", .{});

    // ========================================================================
    // 测试函数作为表达式
    // ========================================================================
    try stdout.print("Functions as expressions:\n", .{});
    const result = add(square(3), square(4));  // 3² + 4² = 9 + 16 = 25
    try stdout.print("  square(3) + square(4) = {d}\n", .{result});

    // ========================================================================
    // 函数组合
    // ========================================================================
    try stdout.print("\nFunction composition:\n", .{});
    const value = double(double(5));  // 5 * 2 * 2 = 20
    try stdout.print("  double(double(5)) = {d}\n", .{value});
}

// ============================================================================
// 运行方式：
//   zig run 012_functions_basics.zig
// ============================================================================

// ============================================================================
// 关键概念解释：
//
// 1. 函数定义语法：
//    fn function_name(param1: type1, param2: type2) return_type {
//        // 函数体
//        return value;
//    }
//
// 2. 函数命名：
//    - 使用 camelCase：myFunction, addNumbers
//    - 函数名应该清晰表达功能
//
// 3. 参数传递：
//    - Zig 默认按值传递
//    - 基本类型直接复制值
//    - 数组、切片等传递引用（后续会讲）
//
// 4. 返回值：
//    - void：无返回值
//    - 其他类型：必须返回相应类型的值
//    - 使用 return 语句返回值
//
// 5. 函数调用：
//    function_name(arg1, arg2)
//    参数数量和类型必须匹配
// ============================================================================

