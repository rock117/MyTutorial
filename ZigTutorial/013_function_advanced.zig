pub fn main() !void {
    const stdout = std.io.getStdOut().writer();

    try stdout.print("=== Advanced Functions Demo ===\n\n", .{});

    // ========================================================================
    // 1. 多返回值测试
    // ========================================================================
    try stdout.print("1. Multiple Return Values:\n", .{});
    const result = divideWithRemainder(17, 5);
    try stdout.print("   17 / 5:\n", .{});
    try stdout.print("   Quotient: {d}\n", .{result.quotient});
    try stdout.print("   Remainder: {d}\n", .{result.remainder});
    try stdout.print("\n", .{});

    // ========================================================================
    // 2. 可选值测试
    // ========================================================================
    try stdout.print("2. Optional Return Values:\n", .{});
    const numbers = [_]i32{ 10, 20, 30, 40, 50 };

    const found = findNumber(&numbers, 30);
    if (found) |index| {
        try stdout.print("   Found 30 at index: {d}\n", .{index});
    } else {
        try stdout.print("   30 not found\n", .{});
    }

    const not_found = findNumber(&numbers, 99);
    if (not_found) |index| {
        try stdout.print("   Found 99 at index: {d}\n", .{index});
    } else {
        try stdout.print("   99 not found\n", .{});
    }
    try stdout.print("\n", .{});

    // ========================================================================
    // 3. 递归函数测试
    // ========================================================================
    try stdout.print("3. Recursive Function (Fibonacci):\n", .{});
    const n: u32 = 10;
    const fib = fibonacci(n);
    try stdout.print("   fibonacci({d}) = {d}\n", .{n, fib});
    try stdout.print("\n", .{});

    // 打印斐波那契数列
    try stdout.print("   Fibonacci sequence:\n", .{});
    var i: u32 = 0;
    while (i <= 10) : (i += 1) {
        const fib_val = fibonacci(i);
        try stdout.print("   F({d}) = {d}\n", .{i, fib_val});
    }
}

