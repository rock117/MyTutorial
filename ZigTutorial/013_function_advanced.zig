const std = @import("std");

// ============================================================================
// 1. 结构体实现多返回值
// ============================================================================
const DivResult = struct {
    quotient: i32,
    remainder: i32,
};

// 返回商和余数
fn divideWithRemainder(a: i32, b: i32) DivResult {
    return .{
        .quotient = @divTrunc(a, b),
        .remainder = @rem(a, b),
    };
}

// ============================================================================
// 2. 可选返回值
// ============================================================================
fn findNumber(arr: []const i32, target: i32) ?usize {
    for (arr, 0..) |value, index| {
        if (value == target) {
            return index;
        }
    }
    return null;
}

