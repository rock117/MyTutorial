// ============================================================================
// 关键概念解释：
//
// 1. 多返回值：
//    - Zig 不支持直接返回多个值
//    - 使用结构体来返回多个相关值
//    - 调用者使用 .field_name 访问各个返回值
//
// 2. 可选返回值 (?T)：
//    - ?T 表示可能返回 T 或 null
//    - 用于表示可能失败的操作
//    - 调用时使用 if 进行模式匹配
//
// 3. 错误返回值 (!T)：
//    - !T 表示可能返回错误或 T 类型的值
//    - 需要使用 try 或 catch 处理
//
// 4. 递归函数：
//    - 函数调用自身
//    - 必须有基准情况（终止条件）
//    - 注意栈溢出风险
//
// 5. 内联函数：
//    - inline 关键字提示编译器展开函数
//    - 减少函数调用开销
//    - 可能增加代码大小
